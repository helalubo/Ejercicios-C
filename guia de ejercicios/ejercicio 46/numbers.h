

/**
*\brief Obtiene un numero random desde cierto numero hasta cierto numero
*\param int desde que numero obtener
*\param int hasta que numero obtener
*\return int numero random
*/

int getRandomNumber(int desde, int hasta);

/**
*\brief Devuelve el mayor numero en un arreglo
*\param int[] arreglo del que se encontrara el mayor numero
*\param int tamanio del arreglo correspondiente
*\return int numero mayor del arreglo
*/

int   getMax(int num[], int max,int tam);

/**
*\brief Devuelve el menor numero en un arreglo
*\param int[] arreglo del que se encontrara el menor numero
*\param int tamanio del arreglo correspondiente
*\return int numero menor del arreglo
*/

int getMin(int num[], int min,int tam);
